function output=convert2voigt(input)
%Convert elastic parameters to voigt form or MT to modified voigt form
if length(input)==21
    output = [ input( 1), input( 2), input( 3), sqrt(2)*input( 4), sqrt(2)*input( 5), sqrt(2)*input( 6); ...
          input( 2), input( 7), input( 8), sqrt(2)*input( 9), sqrt(2)*input(10), sqrt(2)*input(11); ...
          input( 3), input( 8), input(12), sqrt(2)*input(13), sqrt(2)*input(14), sqrt(2)*input(15); ...
          sqrt(2)*input( 4), sqrt(2)*input( 9), sqrt(2)*input(13), 2*input(16), 2*input(17), 2*input(18); ...
          sqrt(2)*input( 5), sqrt(2)*input(10), sqrt(2)*input(14), 2*input(17), 2*input(19), 2*input(20); ...
          sqrt(2)*input( 6), sqrt(2)*input(11), sqrt(2)*input(15), 2*input(18), 2*input(20), 2*input(21) ];
else
    MT=MTcheck(input);
    output=[MT(1,1); MT(2,2); MT(3,3); sqrt(2)*MT(2,3); sqrt(2)*MT(3,1); sqrt(2)*MT(1,2) ];
end
end